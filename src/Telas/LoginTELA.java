    private void tfCnsKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfCnsKeyTyped

        char c = evt.getKeyChar();
        if (((c < '0') || (c > '9')) && (c != KeyEvent.VK_BACK_SPACE)) {
            evt.consume();
        }
        if (tfCns.getText().length() >= 15) {
            evt.consume();
        }

    }//GEN-LAST:event_tfCnsKeyTyped

