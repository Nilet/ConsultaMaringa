    private void botaoEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoEntrarActionPerformed
        String cns, cpf;
        cns = tfCns.getText();
        cpf = tfCpf.getText();

        PacienteDTO objpacientedto = new PacienteDTO();

        objpacientedto.setCns(cns);
        objpacientedto.setCpf(cpf);

        if (objpacientedto.autenticaPaciente()) {
            try {
                Home homeTela = new Home();
                this.dispose();
                homeTela.setVisible(true);
            } catch (Exception e) {
                Home homeTela = new Home();
                this.dispose();
                homeTela.setVisible(true);
            }
        } else {
            JOptionPane.showMessageDialog(null,"Dados Incorretos", "Erro", 0);
        }
    }//GEN-LAST:event_botaoEntrarActionPerformed

